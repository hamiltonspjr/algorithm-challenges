#include "calculator.h"

double sum(double num1, double num2) {
  return num1 + num2;
}

double subtraction(double num1, double num2) {
  return num1 - num2;
}

double multiplication(double num1, double num2) {
  return num1 * num2;
}

double division(double num1, double num2) {
  return num1 / num2;
}